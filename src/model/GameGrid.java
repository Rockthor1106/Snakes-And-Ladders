package model;

public class GameGrid {
	private int rows;
	private int columns;
	private int snakes;
	private int stairs;

	public GameGrid(int rows, int columns) {
		super();
		this.rows = rows;
		this.columns = columns;
	}

	public int getRows() {
		return rows;
	}

	public int getColumns() {
		return columns;
	}

	
}
